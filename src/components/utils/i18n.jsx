// Create a context for language state
const LanguageContext = React.createContext();

// Language Provider component
export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = React.useState('en');

  const changeLanguage = React.useCallback((langCode) => {
    setCurrentLanguage(langCode);
    localStorage.setItem('oneolam-language', langCode);
  }, []);

  React.useEffect(() => {
    const savedLang = localStorage.getItem('oneolam-language');
    if (savedLang && translations[savedLang]) {
      setCurrentLanguage(savedLang);
    }
  }, []);

  const value = React.useMemo(() => ({
    currentLanguage,
    changeLanguage
  }), [currentLanguage, changeLanguage]);

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

// Translation hook
export const useTranslation = () => {
  const context = React.useContext(LanguageContext);

  if (!context) {
    throw new Error('useTranslation must be used within a LanguageProvider');
  }

  const { currentLanguage, changeLanguage } = context;

  const t = React.useCallback((key) => {
    return translations[currentLanguage]?.[key] || translations.en[key] || key;
  }, [currentLanguage]);

  return { t, currentLanguage, changeLanguage };
};
